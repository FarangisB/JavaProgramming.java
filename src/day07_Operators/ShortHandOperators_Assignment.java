package day07_Operators;

public class ShortHandOperators_Assignment {

    public static void main(String[] args) {

        // = assignment operator ( it's not equal sign for Java) // it changes the value of variable without declaring a new primitive type

        int number = 100;

        System.out.println("number = " + number); // 100

        number = 200;

        System.out.println("number = " + number); // 200

        String word = "Java Programming";

        System.out.println("word= " + word); // Java Programming

        //if we'll not use the data anymore and wont to reassign the value, we can just change valuable value

        word = "Wooden Spoon";

        System.out.println("word= " + word); // Wooden Spoon

        word = "Cydeo";

        System.out.println("word= " + word); // Cydeo

        word = "123";

        System.out.println("word" + word);







    }



}
