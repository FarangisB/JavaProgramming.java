package day07_Operators;

public class ShorthandOperators_MultiplicationAssignment {

    public static void main(String[] args) {

        // salary = 50000.50

        double salary = 50000.50;

        System.out.println("Salary = " + salary); // 50000.50

        salary *= 2; // multiplying the salary to double

        System.out.println("Salary = " + salary); // 100001.0

        double doge = 0.001;
        doge *= 1000.0;

        System.out.println("Doge = " + doge);



    }




}
